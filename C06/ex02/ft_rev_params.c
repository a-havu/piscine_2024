#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	k;
	int	i;

	i = argc - 1;
	while (i > 0)
	{
		k = 0;
		while (argv[i][k])
		{
			write(1, &argv[i][k], 1);
			k++;
		}
		write (1, "\n", 1);
		i--;
	}
	return (0);
}
